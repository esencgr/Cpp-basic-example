#include <iostream>
using namespace std;
int main()
{
    cout << "degisken tipleri" << endl;
    //char degisken tipi
    
    char b = 'f';
    
    char s = 's' ;
    
     cout << b << endl;
     cout << s << endl;
    //integer (tamsayi)
    
    int a = 50;
    
    int t;
    t = 60;
    
    cout << a << endl;
    cout << t << endl;
    
    //float-double-long double (ondalik sayilar icin) degisken tipi
    float pi = 3.14;
    
    double c = 21.5;
    
    long double x = 17.5132;
    
    cout << pi << endl;
    cout << c << endl;
    cout << x << endl;
    
    //boolean (true-false -- dogru yanlis) degisken tipi
    bool yil = true;
    
    bool deger = false;
    
    cout << yil << endl;
    cout << deger << endl;
  

    return 0;
}
